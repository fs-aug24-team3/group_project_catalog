export const getColor = (color: string) => {
  switch (color) {
    case 'spacegray':
      return '#4C4C4C';

    case 'silver':
      return '#F0F0F0';

    case 'midnight':
      return '#0A0A0A';

    case 'gold':
      return '#FCDBC1';

    case 'blue':
      return '#182C58';

    case 'red':
      return '#C81111';

    case 'rosegold':
      return '#FADADD';

    case 'skyblue':
      return '#B6DCE9';

    case 'green':
      return '#80DEBF';

    case 'starlight':
      return '#E1D3B7';

    case 'pink':
      return '#F4BDCF';

    case 'black':
      return '#000000';

    case 'purple':
      return '#BF9AF5';

    case 'white':
      return '#FFFFFF';

    case 'yellow':
      return '#F8EB72';

    case 'midnightgreen':
      return '#5F7170';

    case 'coral':
      return '#F67F30';

    case 'spaceblack':
      return '#1E1C1C';

    case 'sierrablue':
      return '#73A1C3';

    case 'graphite':
      return '#5D5C5C';

    default:
      return 'black';
  }
};
